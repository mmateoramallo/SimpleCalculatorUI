    private void btnEqualityValueMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEqualityValueMouseClicked
        // TODO add your handling code here:
        numberTwo.setSecondNumber(Float.parseFloat(lblScreenOperation1.getText()));
        if(null != operador.getOperador()) //De Acuerdo A que operacion se realice
        switch (operador.getOperador()) {
            case "+":
                lblScreenResult.setText(removeZero((float) (numberOne.getNumberOne() + numberTwo.getSecondNumber())));
                break;
            case "-":
                lblScreenResult.setText(removeZero((float) (numberOne.getNumberOne() - numberTwo.getSecondNumber())));
                break;
            case "*":
                lblScreenResult.setText(removeZero((float) (numberOne.getNumberOne() * numberTwo.getSecondNumber())));
                break;
            case "/":
                //Verificar que el divisor no sea igual a cero por que en cuyo caso no se podra efecturar la division
                if(numberTwo.getSecondNumber() == 0){
                    lblScreenResult.setText("!!ERROR No Es Posible Dividir Por Cero");
                }else{
                    lblScreenResult.setText(removeZero((float) (numberOne.getNumberOne() / numberTwo.getSecondNumber())));
                }
                //lblScreenResult.setText(Float.toString((float) (numberOne.getNumberOne() / numberTwo.getSecondNumber())));
                break;
            case "^":
                lblScreenResult.setText(removeZero((float) Math.pow(numberOne.getNumberOne(), numberTwo.getSecondNumber()))); //En donde dice removeZero, es la funcion para sacarle dos decimales a nuestro String en cuyo caso el numero sea .0, iria "Float.toString"
                break;
            default:
                break;
        }
            //Funcion para que se limpie la casilla de operaciones una vez se tenga el resultado en pantalla
            /*
            *Veroficar negando la preposicion en el if si la casilla de resultado no esta vacia, entonces setear la casilla de operacion vacia
            */
            if(!(lblScreenResult.getText().isEmpty())){
                lblScreenOperation1.setText("");
            }
    }//GEN-LAST:event_btnEqualityValueMouseClicked

