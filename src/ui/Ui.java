    //Funcion para quitar el .0 luego de que se realice una operacion con numeros enteros exactos
    public String removeZero(float resultado){
        String retorno = "";
        
        retorno = Float.toString(resultado);
        
        if(resultado%1==0){ //Si el resultado de la division del numero entre 1 da 0 es porque termina en .0 el numero decimal por lo que se ejecutara el codigo
            retorno = retorno.substring(0, retorno.length() -2 );   
        }
        
        return retorno;
    }
    
